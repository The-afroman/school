/*******************************************************************************
 * METHOD Display
 *------------------------------------------------------------------------------
 * displays all atributes of an object of class animal
 * RETURN: NA
 ******************************************************************************/
void Animal::Display() const
{
	/***************************************************************************
	 * CONSTANT
	 *--------------------------------------------------------------------------
	 * ANIMAL_COL - size of animal col
	 * NAME_COL   - size of name col
	 * AGE_COL    - size of age col
	 * VALUE_COL  - size of value col
	 **************************************************************************/
	const int ANIMAL_COL = 11;
	const int NAME_COL   = 15;
	const int AGE_COL    = 3;
	const int VALUE_COL  = 12;

	cout << left;
	cout << setw(ANIMAL_COL) << type;
	cout << setw(NAME_COL)   << name;
	cout << right;
	cout << setw(AGE_COL)    << age;
	cout << fixed << setprecision(2);
	cout << setw(VALUE_COL)  << value << endl;
	cout.unsetf(ios::fixed);
	cout << setprecision(6);
}

