/*******************************************************************************
 * METHOD FindSheep
 *------------------------------------------------------------------------------
 * Finds sheep with specified name
 * RETURN: sheep to search
 ******************************************************************************/
Sheep Farm::FindSheep(string sheepName) const
{
    int i = 0;
    bool found = false;

    while(i < sheepCount-1 && !found)
    {
        if(sheepName == sheepArray[i].GetName())
        {
            found = true;
        }
        else
        {
            i++;
        }
    }
    return(sheepArray[i]);
    
}
